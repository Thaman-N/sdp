def spearman_statistic(x, y):
    return stats.spearmanr(x, y).statistic


def run_permutation_test(model_name, data, n_resamples=10000):
    print(f"\n{'='*60}")
    print(f"PERMUTATION TESTS: {model_name}")
    print(f"{'='*60}")
    print(f"{'Gen':<6} {'Rho':>8} {'Emp p-val':>12} {'Verdict'}")
    print("-" * 45)

    for gen in sorted(data.keys()):
        fim_arr   = np.array(data[gen]["fim"])
        drift_arr = np.array(data[gen]["drift"])

        res = stats.permutation_test(
            (fim_arr, drift_arr),
            spearman_statistic,
            permutation_type='pairings',
            n_resamples=n_resamples,
            alternative='two-sided'
        )
        sig = "* SIGNIFICANT" if res.pvalue < 0.05 else "  not significant"
        print(f"  {gen:<4} {res.statistic:>+8.4f} {res.pvalue:>12.4f}   {sig}")


